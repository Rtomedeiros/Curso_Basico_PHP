<?php
/*
Operadores de Comparação
    == igual
    !=, <> diferente
    === idêntico - Compara o valor e o tipo da variável.
    !== não idêntico
    <
    >
    <=
    >=
*/

$a = 10;
$b = 10;

var_dump($a == $b);
echo "<br><br>";

var_dump($a != $b);
echo "<br><br>";

var_dump($a === $b);
echo "<br><br>";

var_dump($a !== $b);
echo "<br><br>";

var_dump($a < $b);
echo "<br><br>";

var_dump($a > $b);
echo "<br><br>";

var_dump($a <= $b);
echo "<br><br>";

var_dump($a >= $b);
echo "<br><br>";
